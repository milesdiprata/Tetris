int Pieces::GetBlockType(const PieceType pieceType, const int xIndex, const int yIndex, const int rotation)
{
	return pieces[(int)pieceType][rotation][xIndex][yIndex];
}

int Pieces::GetPieceInitialPosX(const PieceType pieceType, const int rotation)
{
	return initialPos[(int)pieceType][rotation][0];
}

int Pieces::GetPieceInitialPosY(const PieceType pieceType, const int rotation)
{
	return initialPos[(int)pieceType][1][rotation];
}
